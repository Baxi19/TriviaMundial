    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Boton para imprimir jugadores
        listModel3.clear();
        
        String Pais = jTextPais.getText();
        int sexo = jComboBoxSexo.getSelectedIndex();
        if(Pais.equals("")){
            JOptionPane.showMessageDialog(this, "Rellene el espacio del Pais.");
        }
        else{
            String Sexo;
            if(sexo==0){
                Sexo="Male";
            }
            else{
                Sexo="Female";
            }
            
            for(int i=0 ; i<Metodos.getInstance().listaUsuarios.size(); i++){
                Usuario u = Metodos.getInstance().listaUsuarios.get(i);
                if(u.getPais().equals(Pais) && u.getSexo().equals(Sexo)){
                    listModel3.addElement(u.getNombreUsuario());
                }
            }
            jListJugadores.setModel(listModel3);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButtonBuscarTorneoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarTorneoActionPerformed
        //Codigo para buscar torneos.
        listModel4.clear();
        String nombre = jTextJugador.getText();
        boolean band=false;
        for(int i = 0 ; i < Metodos.getInstance().getListaTorneos().size() ; i++){//recorrre cada torneo
            for(int j = 0 ; j <Metodos.getInstance().getListaTorneos().get(i).getListaJugadores().size(); j++){// para recorre cada jugador del torneo
                Usuario jugador= Metodos.getInstance().getListaTorneos().get(i).getListaJugadores().get(j);
                if(jugador.getNombreUsuario().equals(nombre)){
                    band=true;
                    break;
                }
            }
            if (band){
                listModel4.addElement(Metodos.getInstance().getListaTorneos().get(i).getNombreTorneo());
                band=false;
            }
        }
        jListTorneo.setModel(listModel4);
        
        
    }//GEN-LAST:event_jButtonBuscarTorneoActionPerformed

