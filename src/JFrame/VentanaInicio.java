    private void jLabelMenuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelMenuMouseClicked
        //controla el movimiento del menu
        if (navBar == true){                
            menu.setSize(50, 0);   
            navBar = false;                
        }else{
            menu.setSize(250, 500);   
            navBar = true; 
        } 
    }//GEN-LAST:event_jLabelMenuMouseClicked

