    public void actualizarPromedios(String torneo){
        Torneo t=Metodos.getInstance().buscarTorneo(torneo);
        for (int j = 0; j < Metodos.getInstance().getListaJugadoresTorneo().size(); j++) {
            Jugador jugador= Metodos.getInstance().getListaJugadoresTorneo().get(j);
            for (int i = 0; i < t.getListaJugadores().size(); i++) {
                if(t.getListaJugadores().get(i).getNombreUsuario().equals(jugador.getNombreUsuario())){
                    t.getListaJugadores().get(i).setPorcentajeAciertos(jugador.getPorcentajeAciertos());
                    break;
                }

            }
        }
    }
    
    public void verificarTiempo(int segundosTorneo){
        if(segundosTorneo >= 3600 ){
            horas++;
            this.segundosTorneo -= 3600;
            verificarTiempo(this.segundosTorneo);
        
        }else if(segundosTorneo >= 60){
            minutos++;
            this.segundosTorneo -= 60;
            verificarTiempo(this.segundosTorneo);
            
        } else if(segundosTorneo > 0) {
            segundos = this.segundosTorneo ;
            this.tiempoConvertido = (horas + " / " + minutos + " / " + segundos);
            jLabelTiempoTotal1.setText(tiempoConvertido);
            return;
        }
        
    
    }
    private void jButtonMostrarEstadisticasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonMostrarEstadisticasMouseClicked
        //metodo para imprimir las estadisticas
        String jugador=jListPlayers.getSelectedValue();
        Jugador j = Metodos.getInstance().buscarJugador(jugador);
        listModel2.clear();
        listModel2.addElement("Puntaje= "+j.getPuntos());
        listModel2.addElement("Monedas= "+ j.getMonedas());
        listModel2.addElement("Saltos de Preguntas= " +(j.getMonedas()/3));
        listModel2.addElement("Porcentaja de aciertos por categoria:");
        for (int i = 0; i < j.getPorcentajeAciertos().size(); i++) {
           Promedio p= j.getPorcentajeAciertos().get(i);
           listModel2.addElement("Categoria : "+p.getCategoria()+ "=   aciertas: "+(p.getCorrectas()/(p.getIncorrectas()+p.getCorrectas()))*100 + "   Desaciertas: "+(p.getIncorrectas()/(p.getCorrectas()+p.getIncorrectas()))*100);
        }
        jListEstadisticas.setModel(listModel2);
   
    }//GEN-LAST:event_jButtonMostrarEstadisticasMouseClicked
    public Jugador buscarganador(){
        Jugador ganador = new Jugador();
        for (int i = 0; i < Metodos.getInstance().getListaJugadoresTorneo().size(); i++) {
            Jugador aux = Metodos.getInstance().getListaJugadoresTorneo().get(i);
            if(aux.getPuntos()>=ganador.getPuntos()){
                ganador = aux;
            }
        }
        return ganador;
    }
    
    

